package pacote;

public class cad_ent extends cad_perg{
	private String nome;
	private String rua;
	private int cpf;
	private String verificar;
	private String verificar_Nome;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public String getVerificar() {
		return verificar;
	}
	public void setVerificar(String verificar) {
		this.verificar = verificar;
	}
	public String getVerificar_Nome() {
		return verificar_Nome;
	}
	public void setVerificar_Nome(String verificar_Nome) {
		this.verificar_Nome = verificar_Nome;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	
	
	
}
